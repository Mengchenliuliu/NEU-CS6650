public class LikeSwipee {

  private String swipee;
  private int number;

  public LikeSwipee(String swipee, int number) {
    this.swipee = swipee;
    this.number = number;
  }

  public String getSwipee() {
    return swipee;
  }

  public void setSwipee(String swipee) {
    this.swipee = swipee;
  }

  public int getNumber() {
    return number;
  }

  public void setNumber(int number) {
    this.number = number;
  }


}
